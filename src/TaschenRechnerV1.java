public class TaschenRechnerV1 {
    public static void main(String[] args) {
        int number1 = 5;
        int number2 = 3;

        System.out.println("Erste Variable " + number1);
        System.out.println("Zweite Variable " + number2);
        System.out.println(number1 + " + " + number2 + " = " + (number1+number2));
        System.out.println(number1 + " - " + number2 + " = " + (number1-number2));
        System.out.println(number1 + " * " + number2 + " = " + (number1*number2));
        System.out.println(number1 + " / " + number2 + " = " + (double) number1/number2);
        System.out.println(number1 + " % " + number2 + " = " + (5%3));

    }






}
